export function getCareerById(id: string): Career | undefined {
  return CAREERS.find((c) => c.id === id);
}

export function getTrendingCareers(): Career[] {
  return CAREERS.filter((c) => c.trending);
}

function parseMultiSelect(raw: string[]): string[] {
  return raw.map((s) => (s.includes("::") ? s.split("::")[1] : s));
}

function parseSingle(raw: string): string {
  return raw.includes("::") ? raw.split("::")[1] : raw;
}

export function matchCareers(
  answers: Record<number, string | string[]>
): Array<Career & { matchScore: number }> {
  const interests = parseMultiSelect((answers[4] as string[]) || []);
  const skills = parseMultiSelect((answers[5] as string[]) || []);
  const goal = parseSingle((answers[3] as string) || "");
  const risk = parseSingle((answers[8] as string) || "");
  const salary = parseSingle((answers[7] as string) || "");

  return CAREERS.map((career) => {
    let score = 0;

    const interestOverlap = career.interests.filter((i) =>
      interests.includes(i)
    ).length;
    score += Math.min(30, interestOverlap * 12);

    const skillOverlap = career.skills.filter((s) => skills.includes(s)).length;
    score += Math.min(25, skillOverlap * 8);

    if (career.goalMatch.includes(goal)) score += 20;
    if (career.riskLevel === risk) score += 15;

    if (salary === "10-20" && career.salaryMax >= 10) score += 5;
    if (salary === "20-50" && career.salaryMax >= 20) score += 5;
    if (salary === "50+" && career.salaryMax >= 50) score += 5;
    if (salary === "5-10" && career.salaryMax >= 5) score += 3;

    const finalScore = Math.max(35, Math.min(98, score + 35));

    return { ...career, matchScore: finalScore };
  })
    .sort((a, b) => b.matchScore - a.matchScore)
    .slice(0, 10);
}
